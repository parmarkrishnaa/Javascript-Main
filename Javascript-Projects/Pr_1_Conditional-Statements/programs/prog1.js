// Grade classification

let marks = 65;

if (marks >= 85) {
    console.log(`Your Grade Is 'A'.`);
} else if (marks >= 70) {
    console.log(`Your Grade Is 'B'.`);
} else if (marks >= 60) {
    console.log(`Your Grade Is 'C'.`);
} else if (marks >= 50) {
    console.log(`Your Grade Is 'D'.`);
} else if (marks = 33) {
    console.log(`You Are Promoted.`);
} else {
    console.log(`You Are Fail.`);
}