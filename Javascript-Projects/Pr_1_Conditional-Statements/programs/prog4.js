// Check if character is vowel or consonant

let char = 'B';

if (
    char == 'A' ||
    char == 'a' ||
    char == 'E' ||
    char == 'e' ||
    char == 'I' ||
    char == 'i' ||
    char == 'O' ||
    char == 'o' ||
    char == 'U' ||
    char == 'u') {
    console.log(`Character ${char} is Vowel.`);
} else {
    console.log(`Character ${char} is Consonant.`);
}