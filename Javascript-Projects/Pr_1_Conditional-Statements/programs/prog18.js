// Age group categorization

let age = 34;

if (age < 13) {
    console.log(`You are a Child.`);
} else if (age < 20) {
    console.log(`You are a Teenager.`);
} else if (age < 35) {
    console.log(`You are at Mid-Age.`);
} else if (age < 55) {
    console.log(`You are an Adult.`);
} else {
    console.log(`You are a Senior Citizen.`);
}