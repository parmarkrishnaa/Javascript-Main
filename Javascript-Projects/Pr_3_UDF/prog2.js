let ch = '+';
let num1 = 5, num2 = 10;

switch(ch){
    case '+':
        console.log(`Sum of ${num1} and ${num2} is`, (num1 + num2), `.`);
    break;
    case '-':
        console.log(`Sum of ${num1} and ${num2} is`, (num1 + num2), `.`);
    break;
    case '/':
        console.log(`Sum of ${num1} and ${num2} is`, (num1 + num2), `.`);
    break;
    case '*':
        console.log(`Sum of ${num1} and ${num2} is`, (num1 + num2), `.`);
    break;
    case '%':
        console.log(`Sum of ${num1} and ${num2} is`, (num1 + num2), `.`);
    break;
    default:
        console.log(`Invalid Choice!!`);
    break;
}